    private void onStart(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onStart
        if (!running) {
            taLog.setText("");
            running = true;
            int numPasswords = 0;
            try {
                numPasswords = Integer.parseInt(tfPass.getText());
            } catch (NumberFormatException e) {
                while ((numPasswords == -1) || (numPasswords > 100) || (numPasswords < 1)) {
                    try {
                        numPasswords = Integer.parseInt(JOptionPane.showInputDialog("Please enter a valid number!"));
                    } catch (NumberFormatException ex) {
                        numPasswords = -1;
                    }
                }
            }
            int numThreads = slThreads.getValue();
            BruteforceManager br = new BruteforceManager(taLog, numPasswords, numThreads);
            Thread t = new Thread((Runnable) br);
            t.start();
        }
        else{
            JOptionPane.showMessageDialog(this, "Another Crack is already running!");
        }

    }//GEN-LAST:event_onStart

